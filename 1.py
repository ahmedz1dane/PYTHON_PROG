print("NUMERIC TYPE:")
a=5
print("Type of a({0}):".format(a), type(a))
b=40
print("Type of b({0}):".format(b), type(b))
c=21+4j
print("Type of c({0}):".format(c), type(c))
print()

print("STRING TYPE:")

d ="Python"
print("Type of d({0}):".format(d), type(d))
print()

print("BOOLEAN TYPE:")

e ="True"
print("Type of e({0}):".format(e), type(e))
print()

print("LIST TYPE")

f=[1,2,3.4]
print("Type of f({0}):".format(f), type(f))
print()

print("TUPLE TYPE:")

g=(1,2,3)
print("Type of g({0}):".format(g), type(g))
print()

print("SET TYPE")

h = {'Karnataka', 'Tamil Nadu' , 'Maharashtra' }
print("Type of h({0}):".format(h), type(h))
print()

print("DICTIONARY TYPE:")
i= {'Karnataka' : 'Bangalore', 'Tamil Nadu' : 'Chennai', 'Maharashtra' : 'Mumbai'}
print("Type of i({0}):".format(i), type(i))
print()

print("BYTES TYPE")
j=b"0x410x420x43"
print("Type of j({0}):".format(j), type(j))
